# Check if running on macOS
check_macos() {
    if [[ "$(uname)" != "Darwin" ]]; then
        echo -e "${RED}‚ùå Error: This script only works on macOS${NC}"
        exit 1
    fi
    echo -e "${GREEN}‚úÖ macOS detected${NC}"
}

# Check if Python 3 is installed
check_python() {
    if ! command -v python3 &> /dev/null; then
        echo -e "${RED}‚ùå Error: Python 3 is not installed${NC}"
        echo -e "${YELLOW}Please install Python 3 first${NC}"
        exit 1
    fi
    echo -e "${GREEN}‚úÖ Python 3 found: $(python3 --version)${NC}"
}

# Download and run the reset script
run_reset() {
    local tmp_script="/tmp/cursor_reset.py"
    local script_url="https://raw.githubusercontent.com/kunnew020/cursor-reset/main/cursor_reset.py"
    
    echo -e "${CYAN}‚¨áÔ∏è  Downloading reset script...${NC}"
    if ! curl -fsSL "$script_url" -o "$tmp_script"; then
        echo -e "${RED}‚ùå Failed to download script${NC}"
        exit 1
    fi
    
    echo -e "${GREEN}‚úÖ Script downloaded${NC}"
    echo ""
    echo -e "${YELLOW}‚ö†Ô∏è  Important:${NC}"
    echo -e "${YELLOW}   ‚Ä¢ Make sure Cursor is CLOSED before continuing${NC}"
    echo -e "${YELLOW}   ‚Ä¢ You will need to enter your admin password${NC}"
    echo -e "${YELLOW}   ‚Ä¢ Backups will be created automatically${NC}"
    echo ""
    
    # Run with sudo
    echo -e "${CYAN}üîê Running reset script with admin privileges...${NC}"
    if sudo python3 "$tmp_script"; then
        echo ""
        echo -e "${GREEN}‚úÖ Reset completed successfully!${NC}"
        echo -e "${CYAN}‚ÑπÔ∏è  Please restart Cursor to apply changes${NC}"
    else
        echo ""
        echo -e "${RED}‚ùå Reset failed${NC}"
    fi
    
    # Cleanup
    rm -f "$tmp_script"
}

